# ============================================== #
# ================== Model ===================== #
# ============================================== #

Tweet:
  id:, user:, text:
  url: -> "/#{user.name}/tweets/#{id}"

# ============================================= #
# ================== View ===================== #
# ============================================= #

.tweet
  .left.thumb
    = @user.thumb.url
  .right
    .name
      = @user.name
    .text
      = link @tweet.text, to: @tweet.url

# =================================================== #
# ================== Controller ===================== #
# =================================================== #

TweetsController:
  show: ->
    @tweet = Tweet.find params.id
    @user  = @tweet.user
